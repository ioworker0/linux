function usage() {
    echo "Sample for rewriting an autogenerated eBPF lskel headers" >&2
    echo "with a new signature" >&2
    echo "" >&2
    echo "USAGE: header_file sig" >&2
    exit 1
}

ARGC=$#

EXPECTED_ARGS=2

if [ $ARGC -ne $EXPECTED_ARGS ] ; then
    usage
else
    SIG=$(xxd -p $2 | tr -d '\n' | sed 's/\(..\)/\\\\x\1/g')
    sed '/const char opts_sig/,/;/c\\tstatic const char opts_sig[] __attribute__((__aligned__(8))) = "\\\n'"$(printf '%s\n' "$SIG")"'\";' $1
fi
